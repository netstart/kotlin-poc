package ifood.company.controller.language

/*
Relacionais
    == igual á
    != diferente
    > maior que
    < menor que
    >= maior ou igual
    <= menor ou igual

Lógicos
    && e
    || ou

 */

fun main(args: Array<String>) {

    println(5 == 5) // == igual á
    println(5 != 5) // diferente de
    println(5 > 5) // maior que
    println(5 < 5) // menor que
    println(5 >= 5) // maior ou igual a
    println(5 <= 5) // menor ou igual a

    println(5 == 5 && 4 < 5)

    println(5 == 4 || 4 < 5)

}